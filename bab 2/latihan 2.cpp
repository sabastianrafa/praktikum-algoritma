#include <iostream>
using namespace std;
int main(){
	int x = 2;
	cout<<"nilai x = "<<x<<endl;
	x += 3;
	cout<<"setelah x += 3, x = "<<x<<endl<<endl;
	cout<<"nilai x setelah ditambah 2 adalah "<<x<<endl;
	x*=2;
	cout<<"setelah x *= 2, x = "<<x<<endl<<endl;
	cout<<"nilai x setelah dikalikan 2 adalah "<<x<<endl;
	x++;
	cout<<"setelah x++ , x = "<<x<<endl<<endl;
	return 0;
}

